NB_learn<-function(trnData, trnLabel)
{
  j=1
  A=matrix(0,1,576)
  B=matrix(0,1,576)
  while(j<=10)
  {
    i=1
    count=0
    while(i<=1409)
      {
      if(j==trnLabel[i])
      {
       sample=as.matrix(trnData[i,1:576])
       count=count+1
      if(count==1)
      {
        A=sample
      }
      else
      {
        A=A+sample
      }
    }
      i=i+1
    }
    A=(A/count)
    if(j==1)
    {
      B=A
    }
    else
    {
      B=rbind(B,A)
    }
    j=j+1
  }
  k=1
  C=matrix(0,1,576)
  D=matrix(0,1,576)
  while(k<=10)
  {
    m=1
    count1=0
    while(m<=1409)
    {
      if(k==trnLabel[m])
      {
        sample1=((as.matrix(trnData[m,1:576])-B[k])^2)
        count1=count1+1
        if(count1==1)
        {
          C=sample1
        }
        else
        {
          C=C+sample1
        }
      }
      m=m+1 
    }
    C=(C/(count1-1))
    if(k==1)
    {
      D=C
    }
    else
    {
      D=rbind(D,C)
    }
    k=k+1
  }
 return(list(B,D))
}